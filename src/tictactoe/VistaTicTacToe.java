    @Override
    public void mouseClicked(MouseEvent e) {
        int contador = 0;

        for (int i = 0; i < ticTacToe.getAreaJuego().length; i++) {
            for (int j = 0; j < ticTacToe.getAreaJuego().length; j++) {

                if (e.getSource() == panelFondoJuego.getComponent(contador++)) {

                    if (ticTacToe.getAreaJuego()[i][j] == 0) {
                        ticTacToe.turno();
                        ticTacToe.getAreaJuego()[i][j] = ticTacToe.getTurno();
                        actualizar();
                        if (ticTacToe.gane(i, j)) {
                            JOptionPane.showMessageDialog(null, "Gano " + matrizGrafica[i][j].getText());
                            ticTacToe.detenerAreaJuego();
                        } else if (ticTacToe.empate()) {
                            JOptionPane.showMessageDialog(null, "Empate");
                        }
                    }

                }
            }
        }
    }

