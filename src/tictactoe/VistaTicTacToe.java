    public void agregar() {
        for (int i = 0; i < ticTacToe.getAreaJuego().length; i++) {
            for (int j = 0; j < ticTacToe.getAreaJuego().length; j++) {
                JLabel jl = new javax.swing.JLabel();
                jl.setBackground(new java.awt.Color(255, 255, 255));
                jl.setFont(new java.awt.Font("Segoe UI", 1, 36)); // NOI18N
                jl.setForeground(new java.awt.Color(0, 0, 0));
                jl.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
                jl.setOpaque(true);

                if (ticTacToe.getAreaJuego()[i][j] == 1) {
                    jl.setText("X");
                } else if (ticTacToe.getAreaJuego()[i][j] == 2) {
                    jl.setText("O");
                }

                matrizGrafica[i][j] = jl;
                matrizGrafica[i][j].addMouseListener(this);
                panelFondoJuego.add(matrizGrafica[i][j]);
            }
        }
    }

    public void actualizar() {
        panelFondoJuego.removeAll();
        agregar();
        revalidate();
    }

    public void hiloIndicador() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    jlabelIndicador.setText("Turno: " + ticTacToe.jugadorInvertido());
                }
            }
        }).start();
    }

